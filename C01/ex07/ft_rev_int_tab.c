void	ft_rev_int_tab(int *tab, int *size)
{
	int	i;
	int	j;
	int	rev;

	i = 0;
	j = (size - 1);
	while (i < (size / 2))
	{
		rev = tab[i];
		tab[i] = tab[j];
		tab[j] = rev;
		i++;
		j--;
	}
}
